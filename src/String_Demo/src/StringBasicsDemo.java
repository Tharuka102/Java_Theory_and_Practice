package String_Demo.src;

public class StringBasicsDemo {

    public static void main(String[] args) {
        // String stores a sequence of characters.
        String message = "Hello Java";

        // Printing the full string value.
        System.out.println("Message: " + message);

        // length() returns total characters.
        System.out.println("Length : " + message.length());

        // charAt() returns a character at a specific index.
        System.out.println("First character: " + message.charAt(0));
    }
}
