package Java_Basic_Conventions_Demo.src;

public class ConstantsConventionDemo {

    // Constants use UPPER_CASE with underscores
    public static final double PI_VALUE = 3.14159;
    public static final int MAX_LOGIN_ATTEMPTS = 3;

    public static void main(String[] args) {
        System.out.println("PI Value            = " + PI_VALUE);
        System.out.println("Max login attempts  = " + MAX_LOGIN_ATTEMPTS);
    }
}
