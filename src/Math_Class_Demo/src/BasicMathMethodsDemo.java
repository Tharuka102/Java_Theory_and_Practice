package Math_Class_Demo.src;

public class BasicMathMethodsDemo {

    public static void main(String[] args) {
        // abs returns the absolute positive value.
        System.out.println("abs(-25) = " + Math.abs(-25));

        // max returns the larger of two numbers.
        System.out.println("max(10, 20) = " + Math.max(10, 20));

        // min returns the smaller of two numbers.
        System.out.println("min(10, 20) = " + Math.min(10, 20));

        // sqrt returns the square root.
        System.out.println("sqrt(81) = " + Math.sqrt(81));
    }
}
