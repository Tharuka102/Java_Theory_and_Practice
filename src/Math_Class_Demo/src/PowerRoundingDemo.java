package Math_Class_Demo.src;

public class PowerRoundingDemo {

    public static void main(String[] args) {
        // pow raises a number to a power.
        System.out.println("2^5 = " + Math.pow(2, 5));

        // ceil rounds up to the next whole number.
        System.out.println("ceil(4.2) = " + Math.ceil(4.2));

        // floor rounds down to the previous whole number.
        System.out.println("floor(4.9) = " + Math.floor(4.9));

        // round returns the nearest whole number.
        System.out.println("round(4.6) = " + Math.round(4.6));
    }
}
