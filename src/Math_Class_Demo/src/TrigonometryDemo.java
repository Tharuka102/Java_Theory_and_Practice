package Math_Class_Demo.src;

public class TrigonometryDemo {

    public static void main(String[] args) {
        double angle = Math.toRadians(30); // Convert degrees to radians.

        // sin gives the sine value of the angle.
        System.out.println("sin(30) = " + Math.sin(angle));

        // cos gives the cosine value of the angle.
        System.out.println("cos(30) = " + Math.cos(angle));

        // tan gives the tangent value of the angle.
        System.out.println("tan(30) = " + Math.tan(angle));
    }
}
