package JDK_JRE_JVM_Demo.src;

public class CompilationConceptDemo {

    public static void main(String[] args) {
        // Java source code is written in .java files.
        System.out.println("Step 1: Write code in a .java file");

        // The JDK compiler converts source code into bytecode.
        System.out.println("Step 2: javac compiles source into .class bytecode");

        // The JVM runs the bytecode on the current machine.
        System.out.println("Step 3: JVM executes the .class file");
    }
}
